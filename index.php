<div class="container-xl section1">
    <h1 class="pb-2">SÍNDROME DE INTESTINO IRRITABLE (SII)</h1>
    <p>El síndrome de intestino irritable (SII) es uno de los <b>trastornos 
        gastrointestinales (GI) más comunes</b> y el más prevalente de los 
        desórdenes GI funcionales. Afecta hasta el <b>12% de la población 
        general adulta</b>, con una leve predilección por población femenina y 
        menores de 50 años. Se caracteriza por dolor abdominal recurrente y 
        alteración de hábitos intestinales (i.e., estreñimiento, diarrea o ambos), 
        a menudo con distensión abdominal asociada.<span class="numeros-arriba">1,2</span></p>
        <br>
        <h3 class="pb-2">PROBLEMA E IMPACTO</h3>
        <p class="lastp">Los síntomas compatibles con SII son extremadamente comunes en la 
            población general en un momento determinado y se experimentan de 
            forma continua, desde cuadros transitorios benignos hasta un proceso 
            patológico que requiere atención, en cuyo caso se observa un cuadro de 
            presentación crónica, con síntomas fluctuantes en la mayoría de los 
            pacientes. El SII afecta el funcionamiento social, la calidad de vida y la 
            capacidad para trabajar; hasta el <b>25% de los pacientes reporta 
            ausencias laborales</b> relacionadas con la enfermedad y hasta un <b>80%, 
            presentismo en el lugar de trabajo.</b><span class="numeros-arriba">3</span></p>
</div>
<div class="container-xl section2">
    <h3 class="color-white pb-3">DIAGNÓSTICO</h3>
    <p class="color-white">En la tabla se muestran los criterios diagnósticos de ROMA IV para SII.<span class="numeros-arriba">4</span></p>
    <div class="greenbox">
        Dolor abdominal recurrente en promedio al menos 1 día/semana en 
            los últimos 3 meses, asociado con 2 o más de los siguientes criterios:
    </div>
    <p class="color-white">1. Relacionado con la defecación</p>
    <div class="hr"></div>
    <p class="color-white">2. Asociado con un cambio en la frecuencia de las deposiciones</p>
    <div class="hr"></div>
    <p class="color-white">3. Asociado con un cambio en la forma (apariencia) de las heces</p>
    <div class="greenbox">
        Estos criterios deben cumplirse durante los últimos 3 meses con inicio de los síntomas al menos 6 meses antes del diagnóstico.
    </div>
    <br>
    <p class="color-white lastp"><b>Se puede hacer un diagnóstico positivo de SII</b> sobre la base de los 
