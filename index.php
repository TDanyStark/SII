<div class="container-xl section3">
    <h3 class="pb-2">CLASIFICACIÓN</h3>
    <p>El SII se clasifica en subtipos de acuerdo con la anomalía de la consistencia de las heces, que incluye estreñimiento predominante (SII-C, &gt;25% de heces duras [Bristol 1 o 2] y &lt;25% de heces blandas [Bristol 6 o 7]), diarrea predominante (SII-D, &gt;25% de heces blandas y &lt;25% heces duras), hábitos intestinales mixtos (SII-M, &gt;25% heces blandas y &gt;25% heces duras); y sin clasificar (SII-U &lt;25% de heces blandas y &lt;25% de heces duras, o sin representación en los demás subtipos).<span class="numeros-arriba">1,3</span></p>
    <br>
    <div class="fondoblanco">
        <h3 class="">ESCALA DE HECES DE BRISTOL</h3>
    </div>
    <p class="">Tipo 1&nbsp;&nbsp;&nbsp;Trozos duros separados, como nueces, que pasan con dificultad.</p>
    <div class="hr"></div>
    <p class="">Tipo 2&nbsp;&nbsp;&nbsp;Forma de “salchicha” compuesta de fragmentos.</p>
    <div class="hr"></div>
    <p class="">Tipo 3&nbsp;&nbsp;&nbsp;Similar a “salchicha o morcilla”, pero con grietas en la superficie.</p>
    <div class="hr"></div>
    <p class="">Tipo 4&nbsp;&nbsp;&nbsp;Similar a “salchicha o serpiente”, lisa y blanda.</p>
    <div class="hr"></div>
    <p class="">Tipo 5&nbsp;&nbsp;&nbsp;Trozos de masa pastosa con bordes definidos, que se defecan fácilmente.</p>
    <div class="hr"></div>
    <p class="">Tipo 6&nbsp;&nbsp;&nbsp;Fragmentos pastosos con bordes irregulares.</p>
    <div class="hr"></div>
    <p class="lastp">Tipo 7&nbsp;&nbsp;&nbsp;Acuosa, sin porciones sólidas.</p>
</div>

<div class="container-xl section4">
    <h3 class="color-white pb-2">ALTERNATIVAS GENERALES DE TRATAMIENTO</h3>

    <p class="color-white lastp">En general, el abordaje terapéutico del SII debe orientarse a el (los) síntoma(s) predominante(s) en cada paciente. Todo paciente debe recibir asesoría con respecto al ejercicio físico, uso de probióticos, cambios en la dieta (p.ej., dieta FODMAP) y medicamentos de primera línea. Idealmente, la eficacia de los tratamientos debe evaluarse a los 3 meses, al cabo de lo cual las terapias iniciales se pueden descontinuar si no hay respuesta, con escalamiento a medicamentos de segunda línea. La psicoterapia se reserva a pacientes cuyos síntomas resultan refractarios a la farmacoterapia.<span class="numeros-arriba">3</span></p>
